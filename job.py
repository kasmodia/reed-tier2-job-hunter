class Job:

    def __init__(self, job_id, job_title, job_description, employer_name, location, date, url):
        self.job_id = job_id
        self.job_title = job_title
        self.job_description = job_description
        self.employer_name = employer_name
        self.location = location
        self.date = date
        self.url = url

